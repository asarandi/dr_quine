section .text

_fwrite:							; rsi = buffer, rdx = nbyte
	push	rdi
	push	rsi
	push	rbx
	push	rcx
	push	rdx

	mov	rdi, [rel fildes]
	mov	rax, sys_write
	syscall

	pop		rdx
	pop		rcx
	pop		rbx
	pop		rsi
	pop		rdi
	ret

_print_byte:		;al = byte
	push	rsi
	push	rcx
	push	rdx

	and		rax, 255
	xor		rdx, rdx
	mov		rcx, 16
	div		rcx
	lea		rsi, [rel hextable]
	mov		al, byte [rsi + rax]
	mov		dl, byte [rsi + rdx]
	lea		rsi, [rel placeholder]
	mov		byte [rsi + 3], al
	mov		byte [rsi + 4], dl
	mov		rdx, 6
	mov		rcx, body_len
	cmp		rbx, rcx
	jne		_print_comma
	dec		rdx	;no comma for last byte
_print_comma:
	call	_fwrite

	pop		rdx
	pop		rcx
	pop		rsi
	ret

_self_hex:
	xor	rbx, rbx		;rbx = index
_self_hex_loop:
	mov	rcx, body_len
	cmp	rbx, rcx
	jge	_self_hex_done
	xor	rdx, rdx
	mov	rax, rbx
	mov	rcx, xxd_linelen
	div	rcx
	test	rdx, rdx
	jnz		_self_hex_skip_db
	mov		rdx, 3
	lea		rsi, [rel nl_db]
	cmp	rbx, 0				;skip first nl
	jnz	_self_hex_put_nl
	dec	rdx
	inc	rsi
_self_hex_put_nl:
	call	_fwrite
_self_hex_skip_db:
	lea	rsi, [rel body]
	mov	al, byte [rbx + rsi]
	inc		rbx
	call	_print_byte
	jmp		_self_hex_loop

_self_hex_done:
	mov		rdx, 1
	lea		rsi, [rel nl_db]
	call	_fwrite
	ret


_main:
	mov	rdx, open_mode			;param3 = mode = 0644 octal
	mov	rsi, open_flags			;param2 = flags = O_CREAT | O_WRONLY | O_TRUNC
	lea	rdi, [rel grace_kid]	;param1 = file name
	mov	rax, sys_open			;
	syscall
	jc _done					;jump if carry, failed

	mov	[rel fildes], rax		;save fd

	mov	rdi, rax				;param1 = fd
	lea	rsi, [rel body]			;param2 = buf
	mov	rdx, body_len			;param3 = n_byte
	mov	rax, sys_write
	syscall

	call _self_hex

	mov	rdi, [rel fildes]
	mov	rax, sys_close
	syscall

_done:
	mov	rax, sys_exit			;bye bye
	mov	rsi, 0					;exit code
	syscall
	ret


