sys_write	equ 0x2000004
sys_exit	equ 0x2000001
xxd_linelen	equ 12
stdout		equ 1

global _main

section .text

print_out:		; rsi = data, rdx = size
	mov rdi, stdout
	mov rax, sys_write
	syscall
	ret

print_str_db:
	lea	rsi, [rel str_db]
	mov	rdx, 2
	call print_out
	ret

print_space:
	lea	rsi, [rel space]
	mov	rdx, 1
	call print_out
	ret

print_zero_x:
	lea rsi, [rel zero_x]
	mov rdx, 2
	call print_out
	ret

print_comma:
	lea	rsi, [rel comma]
	mov rdx, 1
	call print_out
	ret

print_newline:
	lea rsi, [rel newline]
	mov	rdx, 1
	call print_out
	ret

print_hex_byte:		;al = byte to print
	and	rax, 0xff
	xor rdx, rdx
	mov rcx, 16
	div rcx
	push 0x78656c61	;
	mov	rsi, rsp
	lea rbx, [rel hex_table]
	mov	cl, byte [rbx + rax]
	mov byte [rsi], cl
	mov cl, byte [rbx + rdx]
	mov byte [rsi + 1], cl
	mov rdx, 2
	call print_out
	pop rax
	ret

print_self:
	lea	rsi, [rel self]
	mov	rdx, self_size
	call print_out

	lea r8, [rel self]
	mov r9, self_size
	xor r10, r10		;index
	xor	r11, r11		;flag

	cmp r10, r9
	jae	print_done

hex_loop:
	cmp	r11, 0
	jnz skip_db
	call print_str_db
	call print_space
	call print_space

skip_db:
	mov	r11, 1
	call print_zero_x
	mov al, byte [r8 + r10]
	call print_hex_byte
	inc r10
	cmp r10, r9
	jz print_done
	call print_comma

	mov	rax, r10
	mov	rcx, xxd_linelen
	xor rdx, rdx
	div rcx
	cmp rdx, 0
	jz	print_nl
	call print_space
	jmp hex_loop

print_nl:
	call print_newline
	xor r11, r11
	jmp	hex_loop


print_done:
	call print_newline
	ret

_main:
	call print_self
	mov	rax, sys_exit
	mov	rdi, 0
	syscall

